# These operators compare the values ​​on both sides and determine the relationship between them. Also called relational operators. 
# (Let's assume a=20 and b=10.)

# == Returns true if the two values ​​are equal and false if they are different. (a == b) returns false.
# != Returns true if the two values ​​are not equal and false if they are equal. (a != b) returns true.
# >, >= Returns the value of whether the left-hand value is greater than the right-hand value. >= also covers equality. (a > b) returns true.
# <, <= Returns the value of whether the value on the left is less than the value on the right. <= also covers equality. (a < b) returns false.

a,b=20,10

print(a>b)
print(a<b)

