# Strings in Python are defined as a contiguous set of characters represented in quotation marks. 
# Python allows single or double quotes. 
# The plus (+) sign provides repetition of the asterisk (*) when string concatenation.

str = 'Hello Python!'
 
print(str)          # prints whole string
print(str[0])       # prints first character of string
print(str[2:5])     # prints 3 to 5 characters of string
print(str[2:])      # prints from the 3rd character to the last character.
print(str * 2)      # prints 2 times
print(str + " I will learn Python") # performs the merge